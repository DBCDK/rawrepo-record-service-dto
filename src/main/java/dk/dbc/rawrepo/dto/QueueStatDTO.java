package dk.dbc.rawrepo.dto;

public class QueueStatDTO {

    private String text;
    private int count;
    private String date;

    public QueueStatDTO() {

    }

    public QueueStatDTO(String text, int count, String date) {
        this.text = text;
        this.count = count;
        this.date = date;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
